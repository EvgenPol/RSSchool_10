import UIKit

extension UIFont {
    static func nunito600(_ size: CGFloat) -> UIFont {
        UIFont(name: "Nunito-SemiBold", size: size)!
    }
    
    static func nunito800(_ size: CGFloat) -> UIFont {
        UIFont(name: "Nunito-ExtraBold", size: size)!
    }
}
